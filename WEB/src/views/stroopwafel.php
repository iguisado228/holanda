            <div class="ingredients-section">
                <h2 class="section-subtitle">Ingredients</h2>
                <ul>
                    <li>2 1/4 cups all-purpose flour</li>
                    <li>1/2 cup warm milk</li>
                    <li>1/4 cup sugar</li>
                    <li>1/2 cup butter, melted</li>
                    <li>1 egg</li>
                    <li>1 packet active dry yeast</li>
                    <li>1/2 teaspoon cinnamon</li>
                    <li>For the syrup: 1 cup brown sugar, 3/4 cup butter, 1/3 cup dark corn syrup, 1 teaspoon cinnamon</li>
                </ul>
            </div>
            
            <div class="steps-section">
                <h2 class="section-subtitle">Instructions</h2>
                <ol>
                    <li>Dissolve yeast in warm milk with a teaspoon of sugar. Let stand until foamy.</li>
                    <li>Mix flour, sugar, cinnamon, melted butter, egg and yeast mixture to form a dough.</li>
                    <li>Knead for 5 minutes, then let rise for 1 hour.</li>
                    <li>Divide dough into 16 small balls and press in a waffle iron until golden.</li>
                    <li>While still warm, slice each waffle horizontally and spread with warm syrup.</li>
                    <li>Press halves together and let cool on a wire rack.</li>
                </ol>
            </div>
        </div>
