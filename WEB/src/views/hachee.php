                    <li>Cook onions until soft, sprinkle with flour.</li>
                    <li>Add beef back, pour in broth and vinegar.</li>
                    <li>Add spices, bring to boil then simmer for 2 hours.</li>
                    <li>Remove bay leaves, cloves and berries before serving.</li>
                    <li>Serve with mashed potatoes or bread.</li>
